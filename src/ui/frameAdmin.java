    private void tabelAdminMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelAdminMouseClicked
        // TODO add your handling code here:
        int i = tabelAdmin.getSelectedRow();
        String kode = tabelAdmin.getValueAt(i, 0).toString();
        String usn = tabelAdmin.getValueAt(i, 1).toString();
        String pass = tabelAdmin.getValueAt(i, 2).toString();
        String telp = tabelAdmin.getValueAt(i, 3).toString();
        String email = tabelAdmin.getValueAt(i, 4).toString();
        String gambar = tabelAdmin.getValueAt(i, 5).toString();
        String status = tabelAdmin.getValueAt(i, 6).toString();
        
        txtKodeAdmin.setText(kode);
        txtUsername.setText(usn);
        txtPassword.setText(pass);
        txtTelepon.setText(telp);
        txtEmail.setText(email);
        txtGambar.setText(gambar);
        cmbStatus.setSelectedItem(status);
        
    }//GEN-LAST:event_tabelAdminMouseClicked
    
