    private void tampilDataAdmin(){
        DefaultTableModel kolomTabel = new DefaultTableModel();
        kolomTabel.addColumn("Kode Admin");
        kolomTabel.addColumn("Username");
        kolomTabel.addColumn("Password");
        kolomTabel.addColumn("Telepon");
        kolomTabel.addColumn("Email");
        kolomTabel.addColumn("Gambar");
        kolomTabel.addColumn("Status");
        for(int i = 0; i < obj.listDataKodeAdmin().size(); i++){
            Object[] barisData = {obj.listDataKodeAdmin().get(i),
                obj.listDataUsername().get(i),
                obj.listDataPassword().get(i),
                obj.listDataTelepon().get(i),
                obj.listDataEmail().get(i),
                obj.listDataGambar().get(i),
                obj.listDataStatus().get(i)
            };
            kolomTabel.addRow(barisData);
        }
        tabelAdmin.setModel(kolomTabel);
    }
    
