    private void tampilDataKeuangan(){
        DefaultTableModel kolomTabel = new DefaultTableModel();
        kolomTabel.addColumn("ID KEUANGAN");
        kolomTabel.addColumn("TANGGAL");
        kolomTabel.addColumn("DESKRIPSI");
        kolomTabel.addColumn("PENERIMA");
        kolomTabel.addColumn("KETERANGAN");
        kolomTabel.addColumn("POSISI");
        kolomTabel.addColumn("JUMLAH");
        for (int i = 0; i < obj.listDataIdKeuangan().size(); i++){
            Object[] barisData = {
                obj.listDataIdKeuangan().get(i),
                obj.listDataTanggal().get(i),
                obj.listDataDeskripsi().get(i),
                obj.listDataPenerima().get(i),
                obj.listDataketerangan().get(i),
                obj.listDataPosisi().get(i),
                obj.listDataJumlah().get(i)
            };
            kolomTabel.addRow(barisData);
        }
        tabelKeuangan.setModel(kolomTabel);
    }
    
