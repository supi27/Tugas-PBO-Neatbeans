    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        int i = obj.getIndexArray(txtId.getText());
        if (i > -1){
            obj.ubahDataArray(txtId.getText(), txtTgl.getText(), txtDesk.getText(), txtPenerima.getText(),
                    txtKet.getText(), cmbPos.getSelectedItem().toString(), Integer.valueOf(txtJumlah.getText()));
        }
        tampilDataKeuangan();
    }//GEN-LAST:event_btnUbahActionPerformed

